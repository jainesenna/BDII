-- 8 - Explique o funcionamento de um índice e como ele é implementado no SGBD.

-- Índice é um recurso utilizado pelos BDs para a rápida localização de registros em tabelas.
-- Quando cria um  arquivo  de  índice  para a coluna de uma tabela, 
-- a consulta deixa de ser sequencial e o registro é localizado mais rapidamente. 
-- Os arquivos de índices são menores que as tabelas e, por essa razão, ocupam relativamente pouco espaço na memória principal.

